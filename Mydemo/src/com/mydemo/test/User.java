package com.mydemo.test;

public class User {
	
	// 编号
	private String cid;
	
	// 用户名
	private String name;
	
	/**
	 * 默认构造
	 */
	public User(){}

	public String getCid() {
		return cid;
	}

	public void setCid(String cid) {
		this.cid = cid;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
